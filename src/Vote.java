    private void initialize() throws FileNotFoundException, IOException {
        BufferedReader r1 = new BufferedReader(new FileReader("C:\\Users\\Asus\\Documents\\MY java projects\\VotingManagementSystem\\person1.txt"));
        BufferedReader r2 = new BufferedReader(new FileReader("C:\\Users\\Asus\\Documents\\MY java projects\\VotingManagementSystem\\person2.txt"));
        BufferedReader r3 = new BufferedReader(new FileReader("C:\\Users\\Asus\\Documents\\MY java projects\\VotingManagementSystem\\person3.txt"));
        String s;
        if ((s = r1.readLine()) == null) {
            person1 = 0;
        } else {
            person1 = Integer.parseInt(s);

        }

        if ((s = r2.readLine()) == null) {
            person2 = 0;
        } else {
            person2 = Integer.parseInt(s);

        }

        if ((s = r3.readLine()) == null) {
            person3 = 0;
        } else {
            person3 = Integer.parseInt(s);

        }

        r1.close();
        r2.close();
        r3.close();
    }

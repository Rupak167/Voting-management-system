    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        ButtonGroup b = new ButtonGroup();
        b.add(rdb_1);
        b.add(rdb_2);
        b.add(rdb_3);
        if (rdb_1.isSelected()) {
            person1 = person1 + 1;
            BufferedWriter p1;
            try {
                p1 = new BufferedWriter(new FileWriter("person1.txt"));
                p1.write(String.valueOf(person1));
                p1.close();
            } catch (IOException ex) {
                Logger.getLogger(Vote.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (rdb_2.isSelected()) {
            person2 = person2 + 1;

            BufferedWriter p2;
            try {
                p2 = new BufferedWriter(new FileWriter("person2.txt"));
                p2.write(String.valueOf(person2));
                p2.close();
            } catch (IOException ex) {
                Logger.getLogger(Vote.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (rdb_3.isSelected()) {
            person3 = person3 + 1;
            BufferedWriter p3;
            try {
                p3 = new BufferedWriter(new FileWriter("person3.txt"));
                p3.write(String.valueOf(person3));
                p3.close();
            } catch (IOException ex) {
                Logger.getLogger(Vote.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        JOptionPane.showMessageDialog(null, "Thank you so much!");
        new VoterLogin().setVisible(true);
        this.dispose();

    }//GEN-LAST:event_SubmitActionPerformed
